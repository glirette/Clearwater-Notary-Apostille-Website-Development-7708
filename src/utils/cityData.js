// Get city data with fallback to default if city not found
export const getCityData = (citySlug) => {
  const data = cityData[citySlug];
  
  if (data) {
    return data;
  }
  
  // Try to derive display name from slug
  const displayName = citySlug
    .split('-')
    .map(part => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
    
  // Return default data with city name filled in
  return {
    ...cityData.default,
    name: displayName,
    population: 'N/A',
    metaTitle: cityData.default.metaTitle.replace('{CITY}', displayName),
    metaDescription: cityData.default.metaDescription.replace('{CITY}', displayName),
    description: cityData.default.description.replace('{CITY}', displayName)
  };
};

